/**
 * _isupper - a function that checks for the uppercase character.
 * description - more fucntions and nested loops
 * @c: the uppercase character we are checking for
 * Return: 1 if uppercase, 0 otherwise
 */

int _isupper(int c)
{
	if (c >= 'A' && c <= 'Z')
	{
		return (1);
	}
	else
	{
		return (0);
	}
}

